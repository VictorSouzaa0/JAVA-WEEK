package Mathh;
public class Maath {
    public static void main(String[] args) {


        //Math.max : Retorna o maior o valor entre os valores (x,y)
        System.out.println(Math.max(10,4));

        //Math.min : Retorna o menor valor entre os valores (x,y)
        System.out.println(Math.min(90,89));

        //Math.sqrt : retorna o número dele elevado ao quadrado
        System.out.println(Math.sqrt(81));

        //Math.abs : retorna o número absoluto (positivo) do velor de x:
        System.out.println(Math.abs(-9));

        //Math.random : retorna o número  aletatório entre 0.0 e 1.0
        System.out.println(Math.random());
    }
}